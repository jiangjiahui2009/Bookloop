# 运行时书库：内置 + 用户自定义
# 用户自定义的书有一个额外字段：builtin=False
BOOKS: list[dict] = list(BUILTIN_BOOKS)


def add_custom_book(book: dict) -> None:
    """添加用户自定义书友"""
    BOOKS.append({**book, "builtin": False})


def remove_custom_book(book_id: str) -> bool:
    """删除书友"""
    for i, b in enumerate(BOOKS):
        if b["id"] == book_id:
            BOOKS.pop(i)
            return True
    return False


def update_book_soul(book_id: str, persona: dict) -> bool:
    """更新书友的灵魂数据"""
    for b in BOOKS:
        if b["id"] == book_id:
            b["match_trigger"] = persona.get("match_trigger", b.get("match_trigger", ""))
            b["knowledge_summary"] = persona.get("knowledge_summary", b.get("knowledge_summary", ""))
            b["system_prompt"] = persona.get("system_prompt", b.get("system_prompt", ""))
            b["verified"] = True
            return True
    return False
